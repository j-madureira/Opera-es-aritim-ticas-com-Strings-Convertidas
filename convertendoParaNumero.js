//Adição convertendo Strings para números
var a = "4";
var b = "7"
var c = parseFloat(a) + parseFloat(b);
console.log(c)

//Subtração convertendo Strings para números
var d = "3";
var e = "14";
var f = parseFloat(d) - parseFloat(e);
console.log(f)

//Divisão convertendo Strings para números
var g = "14";
var h = "2"
var i = parseFloat(g) / parseFloat(h)
console.log(i)
